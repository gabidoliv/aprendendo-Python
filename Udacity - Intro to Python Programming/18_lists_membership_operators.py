'''
Container de dados - Lista
Estrutura de dados que é uma sequência ordenada e mutável de elementos

'''
months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December']

print(months[0]) # January
print(months[1]) # February
print(months[7]) # August
print(months[-1]) # December
print(months[25]) # IndexError: list index out of range, exemplo utilizando print para depurar com um pequeno exemplo, mostra se o índice está errado