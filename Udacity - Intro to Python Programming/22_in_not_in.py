# in / not in para retornar um booleano

print("'this' in 'this is a string' ->", 'this' in 'this is a string')

print("'in' in 'this is a string' ->", 'in' in 'this is a string')

print("'isa' in 'this is a string' ->", 'isa' in 'this is a string')

print("5 not in [1, 2, 3, 4, 6] ->", 5 not in [1, 2, 3, 4, 6])

print("5 in [1, 2, 3, 4, 6] ->", 5 in [1, 2, 3, 4, 6])