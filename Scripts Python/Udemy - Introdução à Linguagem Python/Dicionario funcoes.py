meu_dicionario = {"A":"AMEIXA", "B":"BOLA","C":"CACHORRO"}
# Converte dicionário em um tupla, conjunto de dados imutáveis
for i in meu_dicionario.items():
    print(i)

#Função que retorna apenas os valores:
for i in meu_dicionario.values():
    print(i)

#Função que retorna apenas as chaves:
for i in meu_dicionario.keys():
    print(i)